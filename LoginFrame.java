    //Check if the user has started the month
    public void Check_Month_Started() {
        PreparedStatement ps;
        ResultSet rs;
        Calendar calendar = new GregorianCalendar();
        int cDate = calendar.get(Calendar.DAY_OF_MONTH);
        System.out.print(cDate);
        
        String query = "SELECT budget "
                        + "FROM budget join user "
                        + "on budget.userid = user.userid "
                        + "where userName = ? and MONTH(MonthYear) = MONTH(CURRENT_DATE()) "
                        + "AND YEAR(MonthYear) = YEAR(CURRENT_DATE())";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, jTextField_UN.getText());
            
            rs = ps.executeQuery();
            
            if (!rs.next() && cDate > 1) {
                JOptionPane.showMessageDialog(null, "Start the Month");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    

